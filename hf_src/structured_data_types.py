# This is a list:
print('\n')
listvar = ['a', 'b', 3, 4.0, True]
print(listvar, end='\n')


# This is a dictionary:
# To represent tables for example.
print('\n')
dictvar = {'item1': 'a',
           'item2': 'b',
           'item3': 3,
           'item4': 4.0,
           'item5': True}
print(dictvar, end='\n')


# This is a set:
# Sets, can make intersections, unions and differences.
print('\n')
setvar = {'a', 'b', 3, 3, 4.0, 4.0, False}
print(setvar, end='\n')  # Only return unique elements.

# This is a tuple:
# Static, immutable, never changes.
print('\n')
tuplevar = ('a', 'b', 3, 4.0, False)
print(tuplevar, end='\n')
print('\n')
